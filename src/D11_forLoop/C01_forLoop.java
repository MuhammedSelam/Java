package D11_forLoop;

public class C01_forLoop {

    public static void main(String[] args) {

        // kullanicinin verdigi input degerinden baslayip
        // ardisik 10 tam sayi yazdirin

        int input = 15;

        for (int i = input; i < input + 10 ; i++) {
            System.out.print(i + " ");
        }


    }
}
