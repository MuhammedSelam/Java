package D02_dataTurleri;

public class C01_dataTurleri {

    public static void main(String[] args) {

        int sayi = 12;
        System.out.println(sayi); // 12

        /*

        int : Data turu
        sayi : Variable ismi
        = : Atama (assingment)
        12 : Variable degeri
        ; : Islem sonu

        Declaration (tanimlama) : esitligin sol tarafi
        Deger atama (assingment) : esitlik ve esitligin sag tarafi

         */
        String metin = "Merhaba Mehmet";
        System.out.println(metin); // Merhaba Mehmet

        boolean metinMi = true;
        boolean sayiMi = false;

        // tam sayilar
        byte a = 127;
        short b = 30000;
        int c = 2185756;
        long d = 9541236564566l;

        // ondalikli sayilar
        float e = 3.1456f;
        double f = 2.56588899544;

        // karakterler
        char ch = '2';
        char harf = 'C';
        char karakter = '*';

    }
}
